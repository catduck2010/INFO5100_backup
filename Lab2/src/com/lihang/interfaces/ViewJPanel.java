    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) historyTable.getModel();
        dtm.setRowCount(0);

        for (VitalSign vs : vsh.getVitalSignHistory()) {
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
    }

