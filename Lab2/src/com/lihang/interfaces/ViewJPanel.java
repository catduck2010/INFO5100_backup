    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.historyTable.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSign vs = (VitalSign) this.historyTable.getValueAt(selectedRow, 0);

            if (JOptionPane.showConfirmDialog(this, "Are you sure to delete this?", "WARNING",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                vsh.deleteVitalSign(vs);
                loadTable();
                setEmpty();
                // yes option
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Select a row.", "No row selected", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.historyTable.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSign vs = (VitalSign) this.historyTable.getValueAt(selectedRow, 0);

            tempField.setText(vs.getTemperature());
            bpField.setText(vs.getBloodPressure());
            pulseField.setText(vs.getPulse());
            dateField.setText(vs.getDate());
        } else {
            JOptionPane.showMessageDialog(this, "Please Select a row.", "No row selected", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewBtnActionPerformed


